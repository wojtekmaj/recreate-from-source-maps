const findNodeModulesLines = (source) => source.match(/import.*from ('|")([a-z-]*)('|");?/g);

const findNodeModules = (source) => {
  const nodeModulesLines = findNodeModulesLines(source);
  const nodeModules = nodeModulesLines && nodeModulesLines.map((nodeModulesLine) => nodeModulesLine.match(/from ('|")([a-z-]*)('|");?/)[2]);
  return nodeModules || [];
};

module.exports = findNodeModules;
